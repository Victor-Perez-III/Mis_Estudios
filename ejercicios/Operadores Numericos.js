console.log(num);

//Operadores Logicos

//Igualdad no estricta(este comparador no es exacto)
const resultado2 = 5 == 5
// Igualdad estricta (este compara el valor y el tipo de dato)
const resultado = 5 === 6
console.log(resultado);
console.log(resultado2);

//menor y mayor que
const resultado3 = 5 > 6 // menor que
const resultado4 = 2 < 4 // mayor que
console.log(resultado3, resultado4);

//Igualdad
const resultado5 = 5 <= 5
const resultado6 = 5 >= 6

// desigualdad
